import numpy as np
c = np.array([1, 2, 3, 4, 5])
d = np.array([5, 4, 3, 2, 1])
print("10 (різниця):", c - d)
print("10 (сума):", c + d)
print("10 (квадрат кожного елемента):", c**2)
print("10 (попарний добуток):", c * d)
print("10 (попарна частка):", c / d)
print("10 (піднесення до степеня):", c**d)